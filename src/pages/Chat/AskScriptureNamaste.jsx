export default function AskScriptureNamaste() {
  const [messages, setMessages] = useState([]);
  const [language, setLanguage] = useState("en");
  const [loading, setLoading] = useState(false);

  const text = UI_TEXT[language] || UI_TEXT.en;
  const selectedLanguage = useMemo(
    () => LANGUAGES.find((item) => item.code === language) || LANGUAGES[0],
    [language]
  );

  const handleSend = async (question) => {
    const trimmedQuestion = question.trim();
    if (!trimmedQuestion || loading) return;

    setMessages((prev) => [...prev, { role: "user", content: trimmedQuestion }]);
    setLoading(true);

    const typingId = Date.now();
    setMessages((prev) => [
      ...prev,
      {
        role: "assistant",
        content: text.typing(selectedLanguage.label),
        _typing: true,
        _id: typingId,
      },
    ]);

    try {
      const res = await askScripture(
        trimmedQuestion,
        null,
        selectedLanguage.backend
      );
      const data = res.data;

      setMessages((prev) => [
        ...prev.filter((message) => message._id !== typingId),
        {
          role: "assistant",
          content: data.answer,
          references: data.references || [],
          provider: data.provider || "",
        },
      ]);
    } catch (err) {
      setMessages((prev) => [
        ...prev.filter((message) => message._id !== typingId),
        {
          role: "assistant",
          content: err.response?.data?.detail || text.error,
        },
      ]);
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="flex h-screen flex-col overflow-hidden">
      <Navbar />

      <div className="flex min-h-0 flex-1 flex-col">
        <div className="shrink-0 border-b border-amber-100/70 bg-white/90 px-6 py-4 backdrop-blur">
          <div className="flex items-center justify-between gap-4">
            <div>
              <h1 className="text-2xl font-semibold text-amber-900">{text.title}</h1>
              <p className="text-sm text-muted">{text.subtitle}</p>
            </div>

            <div className="flex items-center gap-2">
              <label
                htmlFor="chat-lang"
                className="whitespace-nowrap text-xs font-medium text-amber-700"
              >
                {text.answerIn}
              </label>
              <select
                id="chat-lang"
                value={language}
                onChange={(e) => setLanguage(e.target.value)}
                className="cursor-pointer rounded-lg border border-amber-200 bg-white px-3 py-1.5 text-sm text-amber-900 focus:outline-none focus:ring-2 focus:ring-amber-300"
              >
                {LANGUAGES.map((item) => (
                  <option key={item.code} value={item.code}>
                    {item.label}
                  </option>
                ))}
              </select>
            </div>
          </div>
        </div>

        <ChatContainer
          messages={messages}
          labels={{
            referencedVerses: text.referencedVerses,
            providerLabels: text.providerLabels,
          }}
          emptyState={{
            title: text.emptyTitle,
            subtitle: text.emptySubtitle,
            description: text.emptyDescription,
            suggestions: text.suggestions,
          }}
          onSelectSuggestion={handleSend}
          loading={loading}
        />

        <ChatInputLocalized
          onSend={handleSend}
          loading={loading}
          placeholder={text.placeholder}
          ariaLabel={text.placeholder}
          buttonLabel={text.ask}
        />
      </div>
    </div>
  );
}
